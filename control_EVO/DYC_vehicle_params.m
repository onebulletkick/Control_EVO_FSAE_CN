function veh = DYC_vehicle_params()
% DYC_VEHICLE_PARAMS 统一的DYC车辆模型参数。
% 这些参数供理想响应、MPC、载荷转移和转矩分配共同使用。
% 修改车辆实测参数时，优先在这里改，避免各模块参数不一致。
% 本函数不读取外部状态，保证模型更新、单元测试和代码生成看到同一套参数。
%#codegen

% 代码生成要求结构体字段一次性定型，避免读取字段后再新增字段。
r = 0.260;       % 车轮有效滚动半径，单位m；用于轮端扭矩T和纵向力Fx的换算，Fx = T/r。
tf = 1.21;       % 前轮距，单位m；前轴左右轮中心线间距，用于计算前轴差动扭矩产生的横摆力矩。
tr = 1.20;       % 后轮距，单位m；后轴左右轮中心线间距，用于计算后轴差动扭矩产生的横摆力矩。
lf = 0.77;       % 质心到前轴的距离，单位m；用于二自由度车辆模型和纵向载荷转移计算。
lr = 0.77;       % 质心到后轴的距离，单位m；用于二自由度车辆模型和纵向载荷转移计算。
L = lf + lr;     % 轴距，单位m；等于前轴距质心距离与后轴距质心距离之和。
m = 300;         % 整车质量，单位kg；用于车辆横摆动力学、理想响应和载荷转移计算。
Iz = 89.99;      % 绕质心z轴的横摆转动惯量，单位kg*m^2；用于MPC二自由度车辆模型。
Cf = -43214*2;   % 前轴等效侧偏刚度，单位N/rad；负号沿用当前模型侧偏角符号约定，乘2表示左右前轮合计。
Cr = -43214*2;   % 后轴等效侧偏刚度，单位N/rad；负号沿用当前模型侧偏角符号约定，乘2表示左右后轮合计。

veh = struct('r', r, 'tf', tf, 'tr', tr, 'lf', lf, 'lr', lr, ...
    'L', L, 'm', m, 'Iz', Iz, 'Cf', Cf, 'Cr', Cr);
end
